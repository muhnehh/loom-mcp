import { writeFileSync, existsSync, mkdirSync } from 'fs';
import { join } from 'path';

export interface SessionRecord {
  ts: number;
  tool: string;
  target: string;
  tokens_in: number;
  tokens_saved: number;
  latency_ms: number;
}

export interface ReportData {
  session_id: string;
  date: string;
  totals: { tokens_used: number; tokens_saved: number; reduction: number; cost_saved: number; turns: number };
  files_focused: string[];
}

