export async function saveReport(data: ReportData, outputDir: string = '.loom/reports'): Promise<string> {
  if (!existsSync(outputDir)) {
    mkdirSync(outputDir, { recursive: true });
  }
  const filename = `${data.session_id}.html`;
  writeFileSync(join(outputDir, filename), generateReportHTML(data));
  return filename;
}